export type TranslationKey = keyof typeof DICT["en"];

export function translate(key: TranslationKey, lang: Lang, vars?: Record<string, string | number>): string {
  const table = DICT[lang] ?? DICT.en;
  let str: string = (table as Record<string, string>)[key] ?? (DICT.en as Record<string, string>)[key] ?? key;
  if (vars) {
    for (const [k, v] of Object.entries(vars)) {
      str = str.replace(new RegExp(`\\{${k}\\}`, "g"), String(v));
    }
  }
  return str;
}

export function useT() {
  const { state } = useStore();
  const lang = state.settings.language as Lang;
  const t = (key: TranslationKey, vars?: Record<string, string | number>) => translate(key, lang, vars);
  return { t, lang, rtl: lang === "ar" };
}
